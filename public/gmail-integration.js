// Gmail Integration - Send emails directly from NOVA
(function() {
  'use strict';
  
  // Using Gmail API via Google's OAuth 2.0
  window.GmailIntegration = {
    clientId: null, // Will be set via settings
    accessToken: localStorage.getItem('nova_gmail_token'),
    
    // Initialize with client ID
    setClientId(id) {
      this.clientId = id;
      localStorage.setItem('nova_gmail_client_id', id);
    },
    
    // OAuth flow
    async authenticate() {
      const clientId = this.clientId || localStorage.getItem('nova_gmail_client_id');
      if (!clientId) {
        alert('Please set your Google Client ID first');
        return false;
      }
      
      // Use the Render deployment URL (no trailing slash)
      const redirectUri = encodeURIComponent('https://nova-ai-yhow.onrender.com');
      
      const scope = encodeURIComponent('https://www.googleapis.com/auth/gmail.send https://www.googleapis.com/auth/gmail.readonly');
      
      const authUrl = `https://accounts.google.com/o/oauth2/v2/auth?client_id=${clientId}&redirect_uri=${redirectUri}&response_type=token&scope=${scope}&include_granted_scopes=true&state=gmail_auth`;
      
      // Show user what to configure
      alert('Add this to Google Cloud Console → Authorized redirect URIs:\n\nhttps://nova-ai-yhow.onrender.com\n\nThen try again.');
      console.log('Configure this redirect URI in Google Cloud:', 'https://nova-ai-yhow.onrender.com');
      
      // Open popup for OAuth
      const popup = window.open(authUrl, 'gmail_auth', 'width=500,height=600');
      
      // Listen for token
      return new Promise((resolve) => {
        const checkToken = setInterval(() => {
          const token = localStorage.getItem('nova_gmail_token');
          if (token && token !== this.accessToken) {
            this.accessToken = token;
            clearInterval(checkToken);
            if (popup) popup.close();
            resolve(true);
          }
        }, 1000);
        
        setTimeout(() => {
          clearInterval(checkToken);
          resolve(false);
        }, 120000); // 2 minute timeout
      });
    },
    
    // Send email using Gmail API
    async sendEmail(to, subject, body, htmlBody = null) {
      if (!this.accessToken) {
        const authed = await this.authenticate();
        if (!authed) throw new Error('Not authenticated');
      }
      
      // Create email content
      const email = [
        'Content-Type: text/html; charset=utf-8',
        'MIME-Version: 1.0',
        `To: ${to}`,
        `Subject: ${subject}`,
        '',
        htmlBody || body.replace(/\n/g, '<br>')
      ].join('\r\n');
      
      // Base64 encode
      const encodedEmail = btoa(email).replace(/\+/g, '-').replace(/\//g, '_').replace(/=+$/, '');
      
      const res = await fetch('https://gmail.googleapis.com/gmail/v1/users/me/messages/send', {
        method: 'POST',
        headers: {
          'Authorization': `Bearer ${this.accessToken}`,
          'Content-Type': 'application/json'
        },
        body: JSON.stringify({ raw: encodedEmail })
      });
      
      if (!res.ok) {
        const err = await res.json();
        throw new Error(err.error?.message || 'Failed to send email');
      }
      
      return await res.json();
    },
    
    // Send AI response via email
    async sendChatViaEmail(to, chatHistory) {
      let html = '<h2>NOVA AI Conversation</h2><hr>';
      
      chatHistory.forEach(msg => {
        const role = msg.role === 'user' ? 'You' : 'NOVA';
        const color = msg.role === 'user' ? '#333' : '#6d28d9';
        html += `<p><strong style="color:${color}">${role}:</strong> ${msg.content.replace(/\n/g, '<br>')}</p>`;
      });
      
      html += '<hr><p style="color:#999;font-size:12px;">Sent from NOVA AI</p>';
      
      return await this.sendEmail(to, 'NOVA AI Conversation', '', html);
    },
    
    // Send summary email
    async sendSummary(to, summary) {
      return await this.sendEmail(
        to,
        'AI Summary from NOVA',
        summary,
        `<div style="font-family:system-ui,sans-serif;max-width:600px;">
          <h2 style="color:#6d28d9;">🤖 NOVA AI Summary</h2>
          <div style="background:#f5f5f5;padding:20px;border-radius:8px;">
            ${summary.replace(/\n/g, '<br>')}
          </div>
          <p style="color:#999;font-size:12px;margin-top:20px;">
            Generated by NOVA AI on ${new Date().toLocaleString()}
          </p>
        </div>`
      );
    }
  };
  
  // Handle OAuth callback
  if (window.location.hash.includes('access_token') && window.location.hash.includes('gmail_auth')) {
    const params = new URLSearchParams(window.location.hash.substring(1));
    const token = params.get('access_token');
    if (token) {
      localStorage.setItem('nova_gmail_token', token);
      window.close();
    }
  }
  
  // Open Gmail integration modal
  window.openGmailModal = function() {
    const modal = document.createElement('div');
    modal.className = 'modal-overlay open';
    modal.style.zIndex = '600';
    modal.id = 'gmail-modal';
    
    const isConnected = !!GmailIntegration.accessToken;
    
    modal.innerHTML = `
      <div class="modal" onclick="event.stopPropagation()" style="max-width: 500px;">
        <div class="modal-top"><h2>📧 Gmail Integration</h2><button class="modal-close" onclick="closeGmailModal()">&times;</button></div>
        <div class="modal-body">
          <p style="color: var(--muted); margin-bottom: 16px;">Send emails and chat summaries directly from NOVA.</p>
          
          ${!isConnected ? `
          <div style="margin-bottom: 20px; padding: 16px; background: var(--surface2); border-radius: var(--radius);">
            <label style="display: block; margin-bottom: 8px; color: var(--text); font-weight: 500;">Google Client ID</label>
            <input type="text" id="gmail-client-id" placeholder="your-client-id.apps.googleusercontent.com" style="width: 100%; padding: 10px; background: var(--surface); border: 1px solid var(--border); border-radius: var(--radius-sm); color: var(--text); margin-bottom: 8px;">
            <p style="font-size: 11px; color: var(--muted);">Get this from <a href="https://console.cloud.google.com" target="_blank" style="color: var(--accent);">Google Cloud Console</a></p>
            <button onclick="saveGmailClientId()" style="margin-top: 8px; padding: 8px 16px; background: var(--accent); border: none; border-radius: var(--radius-sm); color: #000; font-weight: 600; cursor: pointer; font-size: 12px;">Save & Connect</button>
          </div>
          ` : `
          <div style="margin-bottom: 20px; padding: 16px; background: var(--accent-glow); border-radius: var(--radius); border: 1px solid rgba(139, 92, 246, 0.2);">
            <div style="display: flex; align-items: center; gap: 8px; margin-bottom: 12px;">
              <span style="color: #4ade80;">✓</span>
              <span style="color: var(--text); font-weight: 500;">Gmail connected!</span>
            </div>
            <button onclick="GmailIntegration.accessToken=null;localStorage.removeItem('nova_gmail_token');closeGmailModal();openGmailModal();" style="padding: 6px 12px; background: var(--red); border: none; border-radius: var(--radius-sm); color: #fff; font-size: 11px; cursor: pointer;">Disconnect</button>
          </div>
          
          <!-- Quick Actions -->
          <div style="margin-bottom: 16px; padding: 12px; background: var(--surface2); border-radius: var(--radius); border: 1px solid var(--border);">
            <h4 style="margin-bottom: 10px; color: var(--text); font-size: 13px;">🤖 AI Commands (say these to NOVA)</h4>
            <div style="font-size: 12px; color: var(--muted); line-height: 1.6;">
              • "Send this to email"<br>
              • "Email this conversation"<br>
              • "Export chat to Google Sheets"<br>
              • "Save this to Drive"
            </div>
          </div>
          
          <!-- Email Compose Section -->
          <div style="margin-bottom: 16px; padding: 16px; background: var(--surface2); border-radius: var(--radius); border: 1px solid var(--border);">
            <h4 style="margin-bottom: 12px; color: var(--text); font-size: 14px;">📧 Send Email</h4>
            <div style="display: flex; flex-direction: column; gap: 8px;">
              <input type="email" id="gmail-to" placeholder="recipient@example.com" style="width: 100%; padding: 10px; background: var(--surface); border: 1px solid var(--border); border-radius: var(--radius-sm); color: var(--text);">
              <input type="text" id="gmail-subject" placeholder="Email subject" style="width: 100%; padding: 10px; background: var(--surface); border: 1px solid var(--border); border-radius: var(--radius-sm); color: var(--text);">
              <textarea id="gmail-body" placeholder="Message..." style="width: 100%; height: 100px; padding: 10px; background: var(--surface); border: 1px solid var(--border); border-radius: var(--radius-sm); color: var(--text); resize: vertical;"></textarea>
              <button onclick="sendGmailMessage()" style="padding: 10px; background: var(--accent); border: none; border-radius: var(--radius-sm); color: #000; font-weight: 600; cursor: pointer;">Send Email</button>
            </div>
          </div>
          
          <!-- Quick Email Actions -->
          <div style="display: grid; grid-template-columns: 1fr 1fr; gap: 8px;">
            <button onclick="emailChatSummary()" style="padding: 10px; background: var(--surface2); border: 1px solid var(--border); border-radius: var(--radius-sm); color: var(--text); cursor: pointer; font-size: 12px;">📧 Email This Chat</button>
            <button onclick="emailLastResponse()" style="padding: 10px; background: var(--surface2); border: 1px solid var(--border); border-radius: var(--radius-sm); color: var(--text); cursor: pointer; font-size: 12px;">� Email Last Response</button>
          </div>
          `}
        </div>
      </div>
    `;
    
    modal.onclick = closeGmailModal;
    document.body.appendChild(modal);
  };
  
  window.closeGmailModal = function() {
    const modal = document.getElementById('gmail-modal');
    if (modal) {
      modal.classList.remove('open');
      setTimeout(() => modal.remove(), 300);
    }
  };
  
  window.saveGmailClientId = async function() {
    const id = document.getElementById('gmail-client-id').value;
    if (!id) return;
    
    GmailIntegration.setClientId(id);
    showToast('Connecting to Gmail...');
    
    const success = await GmailIntegration.authenticate();
    if (success) {
      showToast('Gmail connected!');
      closeGmailModal();
      openGmailModal();
    }
  };
  
  window.sendGmailMessage = async function() {
    const to = document.getElementById('gmail-to').value;
    const subject = document.getElementById('gmail-subject').value;
    const body = document.getElementById('gmail-body').value;
    
    if (!to || !subject || !body) {
      alert('Please fill in all fields');
      return;
    }
    
    try {
      showToast('Sending email...');
      await GmailIntegration.sendEmail(to, subject, body);
      showToast('Email sent!');
      closeGmailModal();
    } catch (err) {
      showToast(err.message, 'error');
    }
  };
  
  window.emailLastResponse = async function() {
    const to = document.getElementById('gmail-to')?.value;
    if (!to) {
      alert('Please enter recipient email in the field above');
      return;
    }
    
    // Find last AI response
    let lastAiMsg = null;
    for (let i = state.history.length - 1; i >= 0; i--) {
      if (state.history[i].role === 'ai') {
        lastAiMsg = state.history[i];
        break;
      }
    }
    
    if (!lastAiMsg) {
      alert('No AI response found to email');
      return;
    }
    
    try {
      showToast('Sending last response...');
      await GmailIntegration.sendEmail(
        to,
        'NOVA AI Response',
        lastAiMsg.content,
        `<div style="font-family:system-ui,sans-serif;max-width:600px;">
          <h3 style="color:#6d28d9;">🤖 NOVA AI Response</h3>
          <div style="background:#f5f5f5;padding:20px;border-radius:8px;white-space:pre-wrap;">
            ${lastAiMsg.content.replace(/\n/g, '<br>')}
          </div>
          <p style="color:#999;font-size:12px;margin-top:20px;">
            Sent from NOVA AI on ${new Date().toLocaleString()}
          </p>
        </div>`
      );
      showToast('Last response sent!');
      closeGmailModal();
    } catch (err) {
      showToast(err.message, 'error');
    }
  };
  
  // AI Natural Language Command Detection
  window.detectAndHandleAICommands = function(userMessage, aiResponse) {
    const msg = userMessage.toLowerCase();
    
    // Email commands
    if (msg.includes('send this to email') || msg.includes('email this') || msg.includes('email this conversation') || msg.includes('send to email')) {
      if (GmailIntegration.accessToken) {
        // Ask for email if not set
        const email = prompt('Enter recipient email:');
        if (email) {
          GmailIntegration.sendChatViaEmail(email, state.history)
            .then(() => showToast('Chat sent to ' + email))
            .catch(err => showToast(err.message, 'error'));
        }
        return true; // Command handled
      } else {
        showToast('Gmail not connected. Click 📧 Gmail button first.', 'error');
        return true;
      }
    }
    
    // Sheets export commands
    if (msg.includes('export to sheets') || msg.includes('export chat to sheets') || msg.includes('save to sheets') || msg.includes('google sheets')) {
      if (GoogleIntegration.accessToken) {
        GoogleIntegration.exportChatToSheets(state.history)
          .then(result => {
            showToast('Exported to Sheets!');
            window.open(result.url, '_blank');
          })
          .catch(err => showToast(err.message, 'error'));
        return true;
      } else {
        showToast('Google not connected. Click 🔴 Google button first.', 'error');
        return true;
      }
    }
    
    // Drive save commands
    if (msg.includes('save to drive') || msg.includes('upload to drive') || msg.includes('google drive')) {
      if (GoogleIntegration.accessToken) {
        GoogleIntegration.uploadChatAsDoc(state.history)
          .then(() => showToast('Saved to Google Drive!'))
          .catch(err => showToast(err.message, 'error'));
        return true;
      } else {
        showToast('Google not connected. Click 🔴 Google button first.', 'error');
        return true;
      }
    }
    
    // Calendar commands
    if (msg.includes('add to calendar') || msg.includes('create event') || msg.includes('schedule this')) {
      if (GoogleIntegration.accessToken) {
        const summary = prompt('Event title:', aiResponse.slice(0, 50));
        if (summary) {
          const dateInput = prompt('When? (YYYY-MM-DD HH:MM)');
          if (dateInput) {
            const startTime = new Date(dateInput).toISOString();
            const endTime = new Date(new Date(dateInput).getTime() + 60 * 60 * 1000).toISOString();
            GoogleIntegration.createEvent(summary, aiResponse.slice(0, 500), startTime, endTime)
              .then(() => showToast('Event added to calendar!'))
              .catch(err => showToast(err.message, 'error'));
          }
        }
        return true;
      } else {
        showToast('Google not connected. Click 🔴 Google button first.', 'error');
        return true;
      }
    }
    
    return false; // No command detected
  };
  
  // Email the entire chat conversation
  window.emailChatSummary = async function() {
    const to = document.getElementById('gmail-to')?.value;
    if (!to) {
      alert('Please enter recipient email');
      return;
    }
    
    try {
      showToast('Sending chat summary...');
      await GmailIntegration.sendChatViaEmail(to, state.history);
      showToast('Chat sent via email!');
      closeGmailModal();
    } catch (err) {
      showToast(err.message, 'error');
    }
  };
  
  // Add button to integrations
  window.addGmailButton = function() {
    const sidebar = document.querySelector('.d-sidebar-bot');
    if (sidebar) {
      const btn = document.createElement('button');
      btn.className = 'd-plugins-btn';
      btn.innerHTML = '<svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M4 4h16c1.1 0 2 .9 2 2v12c0 1.1-.9 2-2 2H4c-1.1 0-2-.9-2-2V6c0-1.1.9-2 2-2z"/><polyline points="22,6 12,13 2,6"/></svg> Gmail';
      btn.onclick = openGmailModal;
      btn.style.marginBottom = '8px';
      
      sidebar.insertBefore(btn, sidebar.firstChild);
    }
  };
  
  // Initialize
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
      setTimeout(window.addGmailButton, 2500);
    });
  } else {
    setTimeout(window.addGmailButton, 2500);
  }
  
  console.log('[Gmail] Module loaded - Send emails from NOVA');
})();
